package com.d_d.praktyki.calculator;

public class Calculator {
    public static double add(double a, double b) { return add(a,b,0); }
    public static double add(double a, double b, double c) { return a+b+c; }
    public static double subtract(double a, double b) { return subtract(a,b,0); }
    public static double subtract(double a, double b, double c) { return a-b-c; }
}
